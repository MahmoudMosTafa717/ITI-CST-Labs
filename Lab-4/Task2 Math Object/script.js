var r = Number(prompt("Enter circle radius:"));
var area = Math.PI * r * r;
alert("Area = " + area);

var num = Number(prompt("Enter a number to get sqrt:"));
alert("Sqrt = " + Math.sqrt(num));

var ang = Number(prompt("Angle (degrees):"));

var rad = ang * (Math.PI / 180);
alert("Cos = " + Math.cos(rad));
